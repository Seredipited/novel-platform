async function main() {
  const conn = await mysql.createConnection({
    host: 'localhost', user: 'root', password: '112233',
    charset: 'utf8mb4', database: 'novel_platform'
  });

  console.log('=== Updating novel covers ===');
  // Each novel gets a unique cover from picsum.photos with different seed
  const coverSeeds = [101, 202, 303, 404, 505, 606, 707, 808];
  for (let i = 0; i < coverSeeds.length; i++) {
    const novelId = i + 1;
    const coverUrl = `https://picsum.photos/seed/novel${coverSeeds[i]}/300/400`;
    await conn.query('UPDATE novel SET cover_img = ? WHERE id = ?', [coverUrl, novelId]);
    console.log(`Novel ${novelId} cover set: ${coverUrl}`);
  }

  console.log('\n=== Inserting all chapters ===');
  // Delete existing chapters first
  await conn.query('DELETE FROM novel_chapter');
  console.log('Old chapters cleared.');

  // Each novel gets 10 chapters
  const chapterNames = [
    '序章 命运的齿轮', '第一章 初露锋芒', '第二章 暗流涌动',
    '第三章 风云际会', '第四章 柳暗花明', '第五章 雷霆万钧',
    '第六章 峰回路转', '第七章 山雨欲来', '第八章 惊天逆转',
    '第九章 新的征程'
  ];

  let totalChapters = 0;
  let chapterId = 1;

  for (let novelId = 1; novelId <= 8; novelId++) {
    for (let chNum = 1; chNum <= 10; chNum++) {
      const title = chapterNames[chNum - 1];
      const content = genContent(novelId * 100 + chNum, chNum);

      // Count characters as word_count (Chinese chars)
      const wordCount = content.replace(/\s/g, '').length;

      await conn.query(
        'INSERT INTO novel_chapter (id,novel_id,title,content,chapter_number,word_count) VALUES(?,?,?,?,?,?)',
        [chapterId, novelId, title, content, chNum, wordCount]
      );
      chapterId++;
      totalChapters++;
    }
    console.log(`Novel ${novelId}: 10 chapters inserted.`);
  }

  // Update novel word counts
  for (let novelId = 1; novelId <= 8; novelId++) {
    const [rows] = await conn.query(
      'SELECT SUM(word_count) as total FROM novel_chapter WHERE novel_id = ?', [novelId]
    );
    const total = rows[0].total || 0;
    await conn.query('UPDATE novel SET word_count = ? WHERE id = ?', [total, novelId]);
    console.log(`Novel ${novelId}: word_count updated to ${total}`);
  }

  // Verify
  const [chapters] = await conn.query(
    'SELECT nc.novel_id, n.title, COUNT(*) as cnt FROM novel_chapter nc JOIN novel n ON n.id=nc.novel_id GROUP BY nc.novel_id'
  );
  console.log('\n=== Chapter summary ===');
  chapters.forEach(r => console.log(`Novel "${r.title}": ${r.cnt} chapters`));

  await conn.end();
  console.log('\nALL DONE!');
}

